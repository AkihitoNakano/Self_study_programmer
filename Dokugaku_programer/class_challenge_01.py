class Apple:
    def __init__(self, weight, color, origin, price):
        self.weight = weight
        self.color = color
        self.origin = origin
        self.price = price

apple1 = Apple(10, "red", "Aomori", 100)
print(apple1.color)
print(apple1.weight)
print(apple1.origin)
print(apple1.price)
